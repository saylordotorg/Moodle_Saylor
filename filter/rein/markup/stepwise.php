    <div class="step ui-widget-content ui-corner-all">
        <div class="step-title">Appear for a Temporary Hearing/Possible Temporary Order Issued</div>
        <div class="step-detail"><p>The survivor will likely wait to have what is called an ''ex parte'' hearing with the judge. The perpetrator is not there.</p>
            <p>The judge reviews the paperwork, and may ask a few questions. If the judge believes the survivor is in immediate danger s/he will issue what is called a temporary order and set a date for another hearing with the perpetrator present for the full order. The temporary order will be good for 14 days or until the hearing date.</p>
            <p>If the judge denies the request for a temporary order, the survivor will still be given a court date to come back for a hearing for a Full SAPO.  A temporary order will NOT be in place.</p>
        </div>
    </div>
    <div id="do-arrow" class="arrow"></div>
    <div class="step ui-widget-content ui-corner-all">
        <div class="step-title">Personal Service on Respondent</div>
        <div class="step-detail">
            <p>Although a Temporary SAPO is effective as soon as it is issued by a judge, the perpetrator must be given the court papers before law enforcement will enforce the order. This is because it would be unfair to expect the perpetrator to abide by an order that s/he doesn't even know about.</p>
            <p>During these 14 days, the perpetrator is served. This means s/he is given notice of the hearing date, and a copy of the survivor's petition.</p>
        </div>
    </div>
